# Process both CSV files
for csv_name in ['prompt_data_250321_16-FD.csv', 'prompt_data_250321_16-FI.csv']:
    csv_path = f'project_260321/data/{csv_name}'
    df = pd.read_csv(csv_path)

    df['image_caption'] = None
    matched = 0
    for idx, row in df.iterrows():
        if pd.notna(row.get('prompt_img')):
            img_file = str(row['prompt_img']).strip()
            if img_file in IMAGE_CAPTIONS:
                df.at[idx, 'image_caption'] = IMAGE_CAPTIONS[img_file]
                matched += 1
            else:
                print(f"  WARNING: No caption for {img_file}")

    df.to_csv(csv_path, index=False, encoding='utf-8-sig')
    print(f"Saved: {csv_name} ({matched}/16 captions added, shape={df.shape})")

print("\nDone.")
